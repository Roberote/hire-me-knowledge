# CRIE UM PROGRAMA QUE LEIA O NOME DE UMA CIDADE
# E DIGA SE ELA COMEÇA OU NÃO COM A PALAVRA "SANTO".

cidade = input('Digite o nome de uma cidade: ').strip() # Lê o nome da cidade e o .strip tira os espaços desnecessários que o usuário colocar.

santo = 'Santo'

# #santomin = (santo.lower()) #Essa variavel deixa tudo em letra minuscula pra depois o cidade.title() deixar com as letras iniciais maiusculas, então é inutil.
cidade_formatada = cidade.title()

# Esse if, começa um "grupo if", que é um if que comporta algumas particularidades dentro da opção que ele tá abrindo.
if cidade_formatada.find(santo) == 0:                                           # Aqui ele abre a explicação de que se a cidade que ele colocou lá (Transformada em cidade_formatada) encontrar a string do que está escrito na variavel "santo", que é 'Santo', ele vai falar que começou com Santo, se não (se aparecer -1 no codigo .find) ele vai dizer que não começa com Santo.
    if cidade_formatada == 'Santos':                                            #O Código de cima fala se começa com santo ou não, SANTOS começa com SANTO, mas não da forma que a gente espera que o programa leia, como Santo André ou cidades do tipo, então aqui a gente abre uma outra opção dentro do primeiro if, dizendo que se (segundo if) cidade_formatada estiver escrito "Santos", ele vai printar dizendo que a cidade não começa com santo mas é santos.
        print('Na verdade essa cidade é  Santos, não começa com "Santo -----"')
    else:                                                                       # Aqui a gente fala, "else" for santos, ou seja, "se não" for Santos, mas é (primeiro if) uma cidade que começa com Santo, então printa que a cidade começa com Santo.
        print(f'A Cidade {cidade_formatada} começa com {santo}')
else:                                                                           # Aqui a gente fala, "else" não é santos, ou seja, "se não" for Santos e TAMBÉM NÃO É (primeiro if) uma cidade que começa com Santo, então printa que a cidade não começa com Santo.
    print(f'A cidade {cidade_formatada} não começa com {santo}')





