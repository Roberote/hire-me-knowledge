# OUTRA MANEIRA DE IDENTIFICAR SE UMA CIDADE COMEÇA COM SANTO:

cidadela = input('Digite o nome de uma cidade: ').strip()

cidadela_formatada = cidadela.title()

if not len(cidadela) <= 0:
    print(f'Você digitou a cidade {cidadela_formatada}')
else:
    print('')

if len(cidadela) == 0:
    print('Você não digitou o nome de uma cidade, tente novamente!')
    quit()

if cidadela_formatada.startswith('Santo'): #Aqui não precisamos de == 0 (nesse caso, com o startswith, == True) pois true or false já direciona o IF, no outro colocamos 0 pq o numero zero não é uma confirmação de se acontece ou não algo para o if entender.
    if cidadela_formatada == 'Santos':
        print('A cidade "Santos" começa com "Santo", mas não é um caso de nome composto (como "Santo André"')
    else:
        print(f'A cidade {cidadela_formatada} começa com "Santo".')
else:
    print(f'A cidade {cidadela_formatada} não começa com "Santo".')

